// Импорт базовой функции для создания контекста из React
import { createContext } from "react";

// Создание объекта контекста с начальными значениями по умолчанию
const dataContext = createContext({
    // Значения по умолчанию используются когда компонент находится вне Provider
    mail: "name@example.com", // Дефолтное значение почты
    text: "some text", // Дефолтный текст
    forceChangeMail: () => {}, // Заглушка для функции изменения почты
});

// Экспорт созданного контекста для использования в других компонентах
export default dataContext;
